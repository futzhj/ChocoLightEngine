// Load Light.dll from exe directory and preload all engine modules
static void loadLightEngine(Lumen::IState *L) {
    // Build path: <exe_dir>/Light.dll
    char dllPath[MAX_PATH];
    DWORD pathLen = GetModuleFileNameA(nullptr, dllPath, MAX_PATH);
    if (pathLen == 0) return;

    // Find last path separator
    char *sep = strrchr(dllPath, '\\');
    if (!sep) sep = strrchr(dllPath, '/');
    if (sep) *(sep + 1) = '\0';
    else dllPath[0] = '\0';
    strncat(dllPath, "Light.dll", MAX_PATH - strlen(dllPath) - 1);

    HMODULE hLight = LoadLibraryA(dllPath);
    if (!hLight) return;  // Light.dll not found, skip silently

    int count = 0;
    for (int i = 0; g_lightModules[i].modName; ++i) {
        auto proc = (Lumen::Function)GetProcAddress(hLight, g_lightModules[i].procName);
        if (proc) {
            L->Require(g_lightModules[i].modName, proc);
            ++count;
        }
    }

    if (count > 0) {
        fprintf(stdout, "[Light] Engine loaded: %d modules from %s\n", count, dllPath);
        fflush(stdout);
    }
}

