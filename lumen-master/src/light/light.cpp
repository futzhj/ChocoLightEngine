// ==================== Pack Header: load encrypted script from exe tail ====================
// Format: [exe][encrypted payload][128-byte header with LMPK magic]

static const int LMPK_HEADER_SIZE = 128;
static const int LMPK_OPCODE_COUNT = 38;
static const int LMPK_XOR_KEY_MAX = 64;
static const unsigned int LMPK_MAGIC = 0x4B504D4C; // "LMPK" little-endian

// Master key (obfuscated: each byte XOR'd with its index)
// Matches lightpack.py _MK_ENC
static const unsigned char g_mkEnc[] = {
    0x43^0, 0x68^1, 0x6F^2, 0x63^3, 0x6F^4, 0x4C^5, 0x69^6, 0x67^7,
    0x68^8, 0x74^9, 0x32^10, 0x30^11, 0x32^12, 0x36^13, 0x21^14, 0x40^15,
    0x23^16
};
static const int g_mkLen = sizeof(g_mkEnc);

static unsigned char deobfMK(int i) {
    return g_mkEnc[i] ^ (unsigned char)i;
}

// Remap opcodes in a Lua 5.1 function prototype (recursive)
static void remapProtoOpcodes(unsigned char *buf, int &pos, int len,
                              const unsigned char *revPerm, int szT) {
    auto readByte = [&]() -> int { return (pos < len) ? buf[pos++] : 0; };
    auto readInt = [&]() -> int {
        if (pos + 4 > len) { pos = len; return 0; }
        int v; memcpy(&v, buf + pos, 4); pos += 4; return v;
    };
    auto readNumber = [&]() { pos += 8; };
    auto readString = [&]() {
        if (pos + szT > len) { pos = len; return; }
        size_t sz = 0;
        memcpy(&sz, buf + pos, szT); pos += szT;
        pos += (int)sz;
    };

    readString(); // source name
    readInt(); readInt(); // line defined, last line
    readByte(); readByte(); readByte(); readByte(); // nups, params, vararg, stack

    // Instructions: remap opcode field (bits 0-5)
    int numInstr = readInt();
    int instrStart = pos;
    for (int i = 0; i < numInstr && instrStart + i*4 + 4 <= len; ++i) {
        unsigned int instr;
        memcpy(&instr, buf + instrStart + i*4, 4);
        unsigned int oldOp = instr & 0x3F;
        if (oldOp < (unsigned)LMPK_OPCODE_COUNT) {
            unsigned int newOp = revPerm[oldOp];
            instr = (instr & ~0x3Fu) | newOp;
            memcpy(buf + instrStart + i*4, &instr, 4);
        }
    }
    pos = instrStart + numInstr * 4;

    // Constants
    int numK = readInt();
    for (int i = 0; i < numK; ++i) {
        int t = readByte();
        if (t == 1) readByte();       // boolean
        else if (t == 3) readNumber(); // number
        else if (t == 4) readString(); // string
    }

    // Inner prototypes (recursive)
    int numP = readInt();
    for (int i = 0; i < numP; ++i)
        remapProtoOpcodes(buf, pos, len, revPerm, szT);

    // Debug: lineinfo, locals, upvalues
    int numLine = readInt(); pos += numLine * 4;
    int numLoc = readInt();
    for (int i = 0; i < numLoc; ++i) { readString(); readInt(); readInt(); }
    int numUp = readInt();
    for (int i = 0; i < numUp; ++i) readString();
}

// Try to load packed script from exe's own file tail
static int loadPackedScript(Lumen::IState *L) {
    char exePath[4096];
#ifdef _WIN32
    if (GetModuleFileNameA(nullptr, exePath, sizeof(exePath)) == 0) return -1;
#elif defined(__APPLE__)
    uint32_t epSize = sizeof(exePath);
    if (_NSGetExecutablePath(exePath, &epSize) != 0) return -1;
#elif defined(__linux__) || defined(__ANDROID__)
    ssize_t len = readlink("/proc/self/exe", exePath, sizeof(exePath) - 1);
    if (len <= 0) return -1;
    exePath[len] = '\0';
#else
    return -1; // unsupported platform
#endif

    FILE *f = fopen(exePath, "rb");
    if (!f) return -1;

    // Read 128-byte header from tail
    fseek(f, 0, SEEK_END);
    long fileSize = ftell(f);
    if (fileSize < LMPK_HEADER_SIZE + 16) { fclose(f); return -1; }

    unsigned char hdr[LMPK_HEADER_SIZE];
    fseek(f, fileSize - LMPK_HEADER_SIZE, SEEK_SET);
    if (fread(hdr, 1, LMPK_HEADER_SIZE, f) != LMPK_HEADER_SIZE) { fclose(f); return -1; }

    // Check magic
    unsigned int magic;
    memcpy(&magic, hdr, 4);
    if (magic != LMPK_MAGIC) { fclose(f); return -1; }

    // Parse header fields
    unsigned int payloadSize;
    memcpy(&payloadSize, hdr + 8, 4);

    // Decrypt perm table with master key
    unsigned char perm[LMPK_OPCODE_COUNT];
    for (int i = 0; i < LMPK_OPCODE_COUNT; ++i)
        perm[i] = hdr[16 + i] ^ deobfMK(i % g_mkLen);

    // Build reverse permutation: revPerm[perm[i]] = i
    unsigned char revPerm[LMPK_OPCODE_COUNT];
    memset(revPerm, 0, sizeof(revPerm));
    for (int i = 0; i < LMPK_OPCODE_COUNT; ++i) {
        if (perm[i] < LMPK_OPCODE_COUNT)
            revPerm[perm[i]] = (unsigned char)i;
    }

    // Decrypt XOR key
    unsigned short xkLen;
    memcpy(&xkLen, hdr + 54, 2);
    if (xkLen > LMPK_XOR_KEY_MAX) xkLen = LMPK_XOR_KEY_MAX;
    unsigned char xorKey[LMPK_XOR_KEY_MAX];
    for (int i = 0; i < xkLen; ++i)
        xorKey[i] = hdr[56 + i] ^ deobfMK(i % g_mkLen);

    // Read encrypted payload
    long payloadOffset = fileSize - LMPK_HEADER_SIZE - payloadSize;
    if (payloadOffset < 0) { fclose(f); return -1; }

    unsigned char *payload = (unsigned char *)malloc(payloadSize);
    if (!payload) { fclose(f); return -1; }
    fseek(f, payloadOffset, SEEK_SET);
    if (fread(payload, 1, payloadSize, f) != payloadSize) {
        free(payload); fclose(f); return -1;
    }
    fclose(f);

    // XOR decrypt payload
    for (unsigned int i = 0; i < payloadSize; ++i)
        payload[i] ^= xorKey[i % xkLen];

    // Check if it's bytecode (starts with \033Lua)
    int isBytecode = (payloadSize >= 4 && payload[0] == 0x1b &&
                      payload[1] == 'L' && payload[2] == 'u' && payload[3] == 'a');

    if (isBytecode) {
        // Get size_t width from bytecode header (offset 8)
        int szT = (payloadSize > 8) ? payload[8] : 4;
        // Reverse-remap opcodes back to standard
        int pos = 12; // skip 12-byte luac header
        remapProtoOpcodes(payload, pos, (int)payloadSize, revPerm, szT);
    }

    // Load and execute
    int status = L->LoadBuffer(
        reinterpret_cast<const char*>(payload), payloadSize, "=packed"
    ) || L->TryCall(0, Lumen::RetMul, 0);

    free(payload);
    return status;
}


static int pMain(Lumen::IState *L) {
    auto s = reinterpret_cast<MainArgs *>(L->ToUserdata(1));
    char **argv = s->argv;
    int script;
    int has_i = 0, has_v = 0, has_e = 0;
    globalL = L;
    if (argv[0] && argv[0][0]) programName = argv[0];
    L->GC(Lumen::GCStop, 0);  /* stop collector during initialization */
    L->OpenLibs();  /* open libraries */

#ifdef _WIN32
    loadLightEngine(L);    /* 自动预加载 Light.dll 引擎模块 */
#endif

    L->GC(Lumen::GCRestart, 0);
    s->status = handleLuaInit(L);
    if (s->status != 0) return 0;
    script = collectArgs(argv, &has_i, &has_v, &has_e);
    if (script < 0) {  /* invalid args? */
        printUsage();
        s->status = 1;
        return 0;
    }
    if (has_v) printVersion();
    s->status = runArgs(L, argv, (script > 0) ? script : s->argc);
    if (s->status != 0) return 0;
    if (script)
        s->status = handleScript(L, argv, script);
    if (s->status != 0) return 0;
    if (has_i)
        dotty(L);
    else if (script == 0 && !has_e && !has_v) {
        // No script specified: auto-detect main.lua or lua/main.lua in exe dir
        {
            char exeDir[4096];
            bool gotDir = false;
#ifdef _WIN32
            DWORD edLen = GetModuleFileNameA(nullptr, exeDir, sizeof(exeDir));
            gotDir = (edLen > 0);
#elif defined(__APPLE__)
            uint32_t edSz = sizeof(exeDir);
            gotDir = (_NSGetExecutablePath(exeDir, &edSz) == 0);
#elif defined(__linux__) || defined(__ANDROID__)
            ssize_t edLen = readlink("/proc/self/exe", exeDir, sizeof(exeDir) - 1);
            if (edLen > 0) { exeDir[edLen] = '\0'; gotDir = true; }
#endif
            if (gotDir) {
                char *edSep = strrchr(exeDir, '/');
#ifdef _WIN32
                if (!edSep) edSep = strrchr(exeDir, '\\');
#endif
                if (edSep) *(edSep + 1) = '\0';
                else { exeDir[0] = '.'; exeDir[1] = '/'; exeDir[2] = '\0'; }

                // Set CWD to exe directory
#ifdef _WIN32
                SetCurrentDirectoryA(exeDir);
#else
                (void)chdir(exeDir);
#endif
                // Try main.lua first, then lua/main.lua
                const char *candidates[] = {"main.lua", "lua/main.lua", nullptr};
                for (int ci = 0; candidates[ci]; ++ci) {
                    FILE *fp = fopen(candidates[ci], "r");
                    if (fp) {
                        fclose(fp);
                        s->status = L->DoFile(candidates[ci]);
                        return 0;
                    }
                }
            }

            // Try packed script from exe tail (LMPK format)
            if (loadPackedScript(L) >= 0) {
                return 0;
            }
        }

#if HAS_EMBEDDED_SCRIPTS
        // Fallback: run embedded main.lua script
        s->status = L->LoadBuffer(
            reinterpret_cast<const char*>(g_embed_main_lua),
            g_embed_main_lua_size, "=main.lua"
        ) || L->TryCall(0, Lumen::RetMul, 0);
        return 0;
#else
        if (lua_stdin_is_tty()) {
            printVersion();
            dotty(L);
        } else doFile(L, nullptr);  /* executes stdin as a file */
#endif
    }
    return 0;
}


int main(int argc, char **argv) {
    int status;
    Lumen::IState *L = Lumen::Open();  /* create state */
    if (L == nullptr) {
        luaMessage(argv[0], "cannot create state: not enough memory");
        return EXIT_FAILURE;
    }
    MainArgs s{argc, argv, 0};
    status = L->TryCall(&pMain, &s);
    report(L, status);
    Lumen::Close(L);
    return (status || s.status) ? EXIT_FAILURE : EXIT_SUCCESS;
}

